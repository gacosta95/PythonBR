#Faça um Programa que peça as 4 notas bimestrais e mostre a média.
nt1 = float (input("Digite a primeira nota: "))#Entrada
nt2 = float (input("Digite a segunda nota: "))#Entrada
nt3 = float (input("Digite a terceira nota: "))#Entrada
nt4 = float (input("Digite a quarta nota: "))#Entrada

soma = nt1+nt2+nt3+nt4 #Processamento
med = soma/4#Processamento 
print("Média: ", med)#Saída